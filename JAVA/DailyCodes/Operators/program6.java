class RelationalOperator{

	public static void main(String[] args){
		
		int x = 8;
		int y = 8;
		System.out.println(x==y); //true
		System.out.println(x!=y); //false
		System.out.println(x<y); //false
		System.out.println(x>y);//false
		System.out.println(x<=y); //true
		System.out.println(x>=y); //true
	}
}
